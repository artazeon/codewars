function isAgeDiverse(list) {
  const ageGroups = {
    teens: false,
    twenties: false,
    thirties: false,
    forties: false,
    fifties: false,
    sixties: false,
    seventies: false,
    eighties: false,
    nineties: false,
    centenarian: false,
  }

  list.forEach(el => {
    if (el.age <= 19) {
      ageGroups.teens = true;
    } else if (el.age <= 29) {
      ageGroups.twenties = true;
    } else if (el.age <= 39) {
      ageGroups.thirties = true;
    } else if (el.age <= 49) {
      ageGroups.forties = true;
    } else if (el.age <= 59) {
      ageGroups.fifties = true;
    } else if (el.age <= 69) {
      ageGroups.sixties = true;
    } else if (el.age <= 79) {
      ageGroups.seventies = true;
    } else if (el.age <= 89) {
      ageGroups.eighties = true;
    } else if (el.age <= 99) {
      ageGroups.nineties = true;
    } else if (el.age >= 100) {
      ageGroups.centenarian = true;
    }
  });

  for (let key in ageGroups) {
    if (!ageGroups[key]) {
      return false;
    }
  }

  return true;
}

